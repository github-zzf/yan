package com.junkj.common.vo;

public class ParamsVo {
	// 当前页
	private int page = 1;
	// 每页条数
	private int rows = 20;
	// 栏目ID,允许用","分开表示多个
	private String typeid;
	// 上级栏目pid
	private String pid;
	// 文章id
	private String aid;
	// 标题长度
	private int titlelen;
	// 标签自定义属性值：头条[h]推荐[c]图片[p]幻灯[f]滚动[s]跳转[j]图文[a]加粗[b]
	private String flag;
	// 文章排序
	private String orderby;
	// 升序降序
	private String orderway;

	public int getPage() {
		return page;
	}

	public void setPage(int page) {
		this.page = page;
	}

	public int getRows() {
		return rows;
	}

	public void setRows(int rows) {
		this.rows = rows;
	}

	public String getTypeid() {
		return typeid;
	}

	public void setTypeid(String typeid) {
		this.typeid = typeid;
	}

	public String getPid() {
		return pid;
	}

	public void setPid(String pid) {
		this.pid = pid;
	}

	public String getAid() {
		return aid;
	}

	public void setAid(String aid) {
		this.aid = aid;
	}

	public int getTitlelen() {
		return titlelen;
	}

	public void setTitlelen(int titlelen) {
		this.titlelen = titlelen;
	}

	public String getFlag() {
		return flag;
	}

	public void setFlag(String flag) {
		this.flag = flag;
	}

	public String getOrderby() {
		return orderby;
	}

	public void setOrderby(String orderby) {
		this.orderby = orderby;
	}

	public String getOrderway() {
		return orderway == null || "".equals(orderway) ? "desc" : orderway;
	}

	public void setOrderway(String orderway) {
		this.orderway = orderway;
	}

}
