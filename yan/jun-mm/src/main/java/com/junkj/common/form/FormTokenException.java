package com.junkj.common.form;

public class FormTokenException extends RuntimeException {
	private static final long serialVersionUID = 1L;

	/**
	 * 错误消息
	 */
	private String msg;

	public FormTokenException(String msg) {
		this.msg = msg;
	}

	@Override
	public String toString() {
		return this.getClass() + "{msg='" + msg + "\'}";
	}
}
